package day11_WarmUpTask;
/*
 write a program that can check if the two characters (chars) are same characters
            if same, print: "Same characters"
            otherwise, print: "Different characters"
 */
public class Same_DifferentCharacter {
    public static void main(String[] args) {
        char a = 'B';
        char b = 66;
        if (a==b){
            System.out.println("Same character");}
        else{
            System.out.println("Different character");}

    }
}
